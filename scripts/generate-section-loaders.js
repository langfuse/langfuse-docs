/**
 * Generates lib/section-loaders.generated.ts with dynamic imports for all content sections
 * (except docs which has its own doc-loaders.client.ts).
 * Run: node scripts/generate-section-loaders.js
 */
const fs = require("fs");
const path = require("path");

const CONTENT_DIR = path.join(__dirname, "..", "content");
const SECTIONS = [
  { dir: "self-hosting", collection: "selfHosting" },
  { dir: "blog", collection: "blog" },
  { dir: "changelog", collection: "changelog" },
  { dir: "guides", collection: "guides" },
  { dir: "faq", collection: "faq" },
  { dir: "integrations", collection: "integrations" },
  { dir: "security", collection: "security" },
  { dir: "library", collection: "library" },
  { dir: "customers", collection: "customers" },
  { dir: "handbook", collection: "handbook" },
  { dir: "marketing", collection: "marketing" },
];

function collectFiles(dir, baseDir, list = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const e of entries) {
    const full = path.join(dir, e.name);
    const rel = path.relative(baseDir, full);
    if (e.isDirectory()) {
      if (e.name.startsWith(".")) continue;
      collectFiles(full, baseDir, list);
    } else if (e.name.endsWith(".mdx") || e.name.endsWith(".md")) {
      const slug = rel.replace(/\.(mdx|md)$/, "").replace(/\\/g, "/");
      const ext = path.extname(e.name);
      list.push({ slug, path: rel.replace(/\\/g, "/"), ext });
    }
  }
  return list;
}

const lines = [
  '"use client";',
  "",
  "import type { ComponentType } from 'react';",
  "",
  "/** Generated by scripts/generate-section-loaders.js */",
  "export const sectionLoaders: Record<",
  "  string,",
  "  Record<string, () => Promise<{ default: ComponentType<{ components?: Record<string, ComponentType> }> }>>",
  "> = {",
];

for (const { dir, collection } of SECTIONS) {
  const sectionPath = path.join(CONTENT_DIR, dir);
  if (!fs.existsSync(sectionPath)) continue;
  const files = collectFiles(sectionPath, sectionPath);
  if (files.length === 0) continue;
  lines.push(`  ${JSON.stringify(collection)}: {`);
  for (const { slug, path: filePath } of files) {
    const slugKey = slug === "index" ? "" : slug;
    const importPath = `@/content/${dir}/${filePath}?collection=${collection}`;
    lines.push(`    ${JSON.stringify(slugKey)}: () => import(${JSON.stringify(importPath)}),`);
  }
  lines.push("  },");
}

lines.push("};");
lines.push("");
lines.push(
  "export function getSectionDocLoader(collection: string, slug: string[]) {"
);
lines.push("  const loaders = sectionLoaders[collection];");
lines.push("  if (!loaders) return undefined;");
lines.push("  const key = slug.length === 0 ? '' : slug.join('/');");
lines.push("  return loaders[key];");
lines.push("}");

const outPath = path.join(__dirname, "..", "lib", "section-loaders.generated.ts");
fs.writeFileSync(outPath, lines.join("\n") + "\n");
console.log("Wrote", outPath);
console.log("Collections:", Object.keys(SECTIONS).length);
console.log("Run again after adding new content.");
console.log("Note: faq has dynamic [tag] routes - add manually if needed.");